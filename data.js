// 抽題邏輯 (帶去重功能)
function getNextLevel(levelNum, usedIds = []) {
    // 過濾掉已回答的題目
    const available = triviaDB.filter(q => !usedIds.includes(q.id));
    
    if (available.length === 0) {
        // 題目用光了，回傳重置關卡
        return { mode: 'quiz', id: levelNum, q: "恭喜！所有題庫已答完！", options: ["重置並重新開始"], a: 0, note: "你真是博學多聞！", reset: true };
    }

    const index = Math.floor(Math.random() * available.length);
    const q = available[index];
    return { mode: 'quiz', id: levelNum, dbId: q.id, q: q.q, options: q.options, a: q.a, note: q.note };
}

function generateBossLevel(id) {
    let shuffled = [...triviaDB].sort(()=>Math.random()-0.5);
    return { mode: 'boss', id, questions: shuffled.slice(0,5), note: "Boss 擊破！" };
}
